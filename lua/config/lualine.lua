function lsp_status()
  local attached_clients = vim.lsp.get_active_clients()
  local out = ""

  for i, client in ipairs(attached_clients) do
    local name = client.config.name

    out = out .. name

    if i ~= #attached_clients then
      out = out .. " "
    end
  end

  return out
end

